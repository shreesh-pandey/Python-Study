# Types of Datatypes

# Text Type
name = "Shreesh" # String

# Numeric Type
age = 30          # int
price = 99.99     # float
z = 5 + 3j        # complex

# Boolean Type
active = True # Boolean

# Sequence Type
fruits = ["apple", "banana", "cherry"]  # list
coordinates = (10, 20)                  # tuple
numbers = range(5)                      # 0 to 4

# Mapping Type
person = {"name": "Shreesh", "age": 30}

# Set Types
unique_items = {"pen", "pencil", "pen"} # Set

# None Type
empty = None