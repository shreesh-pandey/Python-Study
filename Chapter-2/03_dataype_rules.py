# Rules for Naming Variables (Identifiers)

# Must begin with a letter or underscore `_`
# Can include letters, digits, underscores
# Cannot start with a digit
# Cannot use reserved keywords (like `for`, `if`, `class`)

# Examples:

# Valid:
name = "Shreesh"
value = 1
score1 = 351

# Invalid: Remove # to check the errors.
# 1name = "Shreesh"
# @name = "Shree"
# class = 1
