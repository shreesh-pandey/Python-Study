# String Indexing

word = "Python"

# Indexing starts with 0. So to print the first letter, we have to use index as 0.
print(word[0]) # Output will be 'P'

print(word[3]) # Output will be 'h'

# Python also has a concept of Negative Indexing.

print(word[-1])  # Output: 'n'
print(word[-2])  # Output: 'o'